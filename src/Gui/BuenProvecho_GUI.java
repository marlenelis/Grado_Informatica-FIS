    private void jb_registarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_registarActionPerformed
        String nombreRes, nombreRest,localidad,provincia,telefono,direccion;
        
        nombreRes= this.jt_nomResp.getText();
        nombreRest= this.jt_nombreRest.getText();
        localidad = this.jt_localidad.getText();
        direccion =  this.jt_direccion.getText();
        provincia = this.jt_provincia.getText();
        telefono = this.jt_telefono.getText();
        
            try {
                if(nombreRes.equals("")||nombreRest.equals("")|| 
                   localidad.equals("")|| direccion.equals("")||
                   provincia.equals("")||telefono.equals("")){
                    
                    JOptionPane.showMessageDialog(null, "Es necesario rellenar todos los campo ");
                }
                else{
                    aComer.solicitarRegistroRestaurante(nombreRes, nombreRest, provincia, localidad, direccion, telefono);
                    JOptionPane.showMessageDialog(null, "El registro se ha realizado con éxito");
                    this.jt_nomResp.setText("");
                    this.jt_nombreRest.setText("");
                    this.jt_localidad.setText("");
                    this.jt_direccion.setText("");
                    this.jt_provincia.setText("");
                    this.jt_telefono.setText("");
                }
            } catch (Exception ex) {
               JOptionPane.showMessageDialog(null, ex);               
            }
            
    }//GEN-LAST:event_jb_registarActionPerformed

