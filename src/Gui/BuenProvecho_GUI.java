    public int obtenerMes(String mes){
        int _mes=-1;
         switch(mes){
                        case "Enero":
                            _mes = 1;
                            break;                    
                        case "Febrero":
                            _mes = 2;
                            break;
                        case "Marzo":
                            _mes = 3;
                            break;
                        case "Abril":
                            _mes = 4;
                            break;
                        case "Mayo":
                            _mes = 5;
                            break;
                        case "Junio":
                            _mes = 6;
                            break; 
                        case "Julio":
                            _mes = 7;
                            break; 
                        case "Agosto":
                            _mes = 8;
                            break; 
                        case "Septiembre":
                            _mes = 6;
                            break; 
                        case "Octubre":
                            _mes = 9;
                            break; 
                        case "Noviembre":
                            _mes = 10;
                            break; 
                        case "Diciembre":
                            _mes = 6;
                            break; 
                    }
         return _mes;
    }
